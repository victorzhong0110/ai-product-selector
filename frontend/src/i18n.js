export function t(lang, key, vars = {}) {
  const dict = translations[lang] || translations['zh']
  let str = dict[key] || translations['zh'][key] || key
  Object.entries(vars).forEach(([k, v]) => {
    str = str.replace(`{${k}}`, v)
  })
  return str
}
